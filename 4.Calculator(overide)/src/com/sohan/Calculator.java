package com.sohan;

public class Calculator {

    public final int add(int a, int b){
        return a + b;
    }
    public final int substract(int a, int b){
        return a - b;
    }
    public final int multiply(int a, int b){
        return a * b;
    }
    public final int divide(int a, int b){
        return a / b;
    }
}
